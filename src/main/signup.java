    private void rSButtonHover1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rSButtonHover1MouseClicked
        // TODO add your handling code here:
        String sql = "INSERT INTO users (nom,pwd,username) VALUES (?,?,?)";
        Connection conn = null;
        try{
            conn = connexion.getConnexion();
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, txtNom.getText());
            statement.setString(2, String.valueOf(pwdField.getPassword()));
            statement.setString(3, txtUsername.getText());
            statement.execute();
            JOptionPane.showMessageDialog(null, "Utilisateur enregist√©");
        }catch(java.sql.SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            connexion.closeConnexion(conn);
        }
    
    }//GEN-LAST:event_rSButtonHover1MouseClicked

